def convert_file(md_path: str, output_dir: str | None = None,
                 compact: bool = False, level: int | None = None) -> str:
    """Convert a single .md file to .tex, return the output path.

    level: 压缩级别 0-3。如果指定 compact=True 且 level=None，则使用 level=3。
    """
    if level is None:
        level = 3 if compact else 0

    md_path = Path(md_path)
    with open(md_path, 'r', encoding='utf-8') as f:
        md_text = f.read()

    parser = ResumeParser(md_text)
    parser.parse()
    tex_content = emit_latex(parser, level=level)

    if output_dir:
        out_dir = Path(output_dir)
        out_dir.mkdir(parents=True, exist_ok=True)
        out_path = out_dir / md_path.with_suffix('.tex').name
    else:
        out_path = md_path.with_suffix('.tex')

    with open(out_path, 'w', encoding='utf-8') as f:
        f.write(tex_content)

    return str(out_path)


def main():
    ap = argparse.ArgumentParser(description='Convert resume Markdown to LaTeX (XeLaTeX)')
    ap.add_argument('files', nargs='+', help='Markdown resume file(s)')
    ap.add_argument('--output', '-o', default=None, help='Output directory for .tex files')
    ap.add_argument('--compact', action='store_true', help='Use compact spacing (level 3)')
    ap.add_argument('--level', type=int, default=None, choices=[0, 1, 2, 3],
                    help='Compression level: 0=default, 1=single header, 2=+single job, 3=+compact spacing')
    args = ap.parse_args()

    level = args.level
    if level is None and args.compact:
        level = 3

    for md_file in args.files:
        try:
            out = convert_file(md_file, args.output, level=level)
            print(f"✅  {md_file} → {out}")
        except Exception as e:
            print(f"❌  {md_file}: {e}", file=sys.stderr)
            sys.exit(1)


if __name__ == '__main__':
    main()
