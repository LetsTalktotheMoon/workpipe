def emit_latex(parser: ResumeParser, level: int = 0) -> str:
    """Convert parsed resume into a complete LaTeX document string.

    level:
      0 = 默认（两行 header + 两行 job + 正常间距）
      1 = 单行 header（姓名 | 联系方式）
      2 = 单行 header + 单行 job（Title | Company | City | Date）
      3 = 单行 header + 单行 job + 紧凑间距
    """
    compact = level >= 3
    single_header = level >= 1
    single_job = level >= 2
    job_vspace = r'\vspace{4pt}' if compact else r'\vspace{6pt}'
    sub_vspace = r'\vspace{2pt}' if compact else r'\vspace{4pt}'

    parts = [build_preamble(level), r'\begin{document}', '']

    # ─── HEADING ───
    parts.append(r'\begin{center}')
    if single_header:
        # 单行: 姓名(14pt bold) | 联系方式(9.5pt)
        header_items = []
        if parser.name:
            header_items.append(r'{\fontsize{16pt}{19.2pt}\selectfont\bfseries ' +
                                escape_latex(parser.name) + r'}')
        contact_parts = _build_contact_parts(parser)
        if contact_parts:
            header_items.append(r'{\fontsize{10.5pt}{12.6pt}\selectfont ' +
                                r' \hspace{0.4em}\textbar\hspace{0.4em} '.join(contact_parts) +
                                r'}')
        if header_items:
            parts.append(r'  \parbox{\textwidth}{\centering ' +
                         r' \hspace{0.4em}\textbar\hspace{0.4em} '.join(header_items) +
                         r'}')
    else:
        # 两行: 姓名 16pt 一行 + 联系方式 9.5pt 一行
        if parser.name:
            parts.append(r'  {\fontsize{18pt}{21.6pt}\selectfont\bfseries ' +
                         escape_latex(parser.name) + r'} \\[1pt]')
        contact_parts = _build_contact_parts(parser)
        if contact_parts:
            parts.append(r'  {\fontsize{10.5pt}{12.6pt}\selectfont ' +
                         r' \hspace{0.4em}\textbar\hspace{0.4em} '.join(contact_parts) +
                         r'}')
    parts.append(r'\end{center}')
    parts.append('')

    # ─── PROFESSIONAL SUMMARY ───
    if parser.summary_bullets:
        parts.append(r'\section{Professional Summary}')
        parts.append(r'\resumeItemListStart')
        for bullet in parser.summary_bullets:
            parts.append(r'  \resumeItem{' + escape_latex(bullet) + '}')
        parts.append(r'\resumeItemListEnd')
        parts.append('')

    # ─── WORK EXPERIENCE ───
    if parser.work_experience:
        parts.append(r'\section{Work Experience}')
        for job in parser.work_experience:
            # needspace: 保留 header + 至少 1 行 bullet，但不要太大以免大片留白
            parts.append(r'\needspace{2\baselineskip}')
            parts.append(job_vspace)

            title_tex = r'\textbf{\color{sectionblue}' + escape_latex(job['title']) + '}'
            company_tex = r'{\color{sectionblue}' + escape_latex(job['company']) + '}' if job['company'] else ''
            location_tex = (r'{\color{sectionblue}' + escape_latex(job['location']) + '}') if job['location'] else ''
            date_tex = (r'{\color{sectionblue}' + escape_latex(job['date']) + '}') if job['date'] else ''

            # Single line: Job Title | Company · Team | Location | Dates
            job_line_parts = [title_tex]
            if company_tex:
                job_line_parts.append(company_tex)
            if location_tex:
                job_line_parts.append(location_tex)
            if date_tex:
                job_line_parts.append(date_tex)
            
            parts.append(r'{\fontsize{11.5pt}{13.8pt}\selectfont\color{sectionblue} ' + 
                         r' \hspace{0.4em}\textbar\hspace{0.4em} '.join(job_line_parts) + 
                         r'}\par')

            parts.append(r'\vspace{2pt}')

            # 遍历 items: 子项目标题 + bullets
            in_bullet_list = False
            for item_type, item_text in job['items']:
                if item_type == 'subproject':
                    if in_bullet_list:
                        parts.append(r'\resumeItemListEnd')
                        in_bullet_list = False
                    parts.append(r'\needspace{2\baselineskip}')
                    parts.append(sub_vspace)
                    parts.append(r'{\fontsize{10.5pt}{12.6pt}\selectfont\textbf{\textit{' +
                                 escape_latex(item_text) + r'}}}\par')
                    parts.append(r'\vspace{1pt}')
                elif item_type == 'scope':
                    if in_bullet_list:
                        parts.append(r'\resumeItemListEnd')
                        in_bullet_list = False
                    parts.append(r'\vspace{1pt}')
                    # Style: 9pt, italics, no prefix. Preserve internal bolding.
                    parts.append(r'{\fontsize{9pt}{10.8pt}\selectfont\textit{' +
                                 escape_latex(item_text) + r'}}\par')
                    parts.append(r'\vspace{2pt}')
                elif item_type == 'bullet':
                    if not in_bullet_list:
                        parts.append(r'\resumeItemListStart')
                        in_bullet_list = True
                    parts.append(r'  \resumeItem{' + escape_latex(item_text) + '}')
            if in_bullet_list:
                parts.append(r'\resumeItemListEnd')
            parts.append('')

    # ─── PROJECTS ───
    if parser.projects:
        parts.append(r'\section{Projects}')
        for title, bullets in parser.projects:
            parts.append(r'\vspace{4pt}')
            parts.append(r'{\fontsize{11pt}{13.2pt}\selectfont\textbf{' + escape_latex(title) + r'}}\par')
            for item_type, item_text in bullets:
                if item_type == 'scope':
                    if in_bullet_list:
                        parts.append(r'\resumeItemListEnd')
                        in_bullet_list = False
                    parts.append(r'\vspace{1pt}')
                    parts.append(r'{\fontsize{9pt}{10.8pt}\selectfont\textit{' +
                                 escape_latex(item_text) + r'}}\par')
                    parts.append(r'\vspace{2pt}')
                else: # bullet
                    if not in_bullet_list:
                        parts.append(r'\resumeItemListStart')
                        in_bullet_list = True
                    parts.append(r'  \resumeItem{' + escape_latex(item_text) + '}')
            parts.append('')

    # ─── SKILLS ───
    if parser.skills:
        parts.append(r'\section{Skills}')
        parts.append(r'\resumeItemListStart')
        for cat, items in parser.skills:
            parts.append(r'  \resumeItem{\textbf{' + escape_latex(cat) + ':} ' +
                         escape_latex(items) + '}')
        parts.append(r'\resumeItemListEnd')
        parts.append('')

    # ─── EDUCATION ───
    if parser.education:
        parts.append(r'\section{Education}')
        parts.append(r'\resumeItemListStart')
        for degree, institution, period in parser.education:
            parts.append(
                r'  \resumeItem{\textbf{' + escape_latex(degree) + r'} ' +
                r' \hspace{0.4em}\textbar\hspace{0.4em} ' + escape_latex(institution) +
                r' \hspace{0.4em}\textbar\hspace{0.4em} ' + escape_latex(period) + '}'
            )
        parts.append(r'\resumeItemListEnd')
        parts.append('')

    # ─── ADDITIONAL INFORMATION ───
    if parser.additional:
        parts.append(r'\section{Additional Information}')
        parts.append(r'\resumeItemListStart')
        for item in parser.additional:
            parts.append(r'  \resumeItem{' + escape_latex(item) + '}')
        parts.append(r'\resumeItemListEnd')
        parts.append('')

    parts.append(r'\end{document}')
    return '\n'.join(parts)


def _build_contact_parts(parser: ResumeParser) -> list[str]:
    """构建联系方式 LaTeX 片段列表"""
    contact_parts = []
    if parser.email:
        contact_parts.append(
            r'\href{mailto:' + parser.email + r'}{\underline{' + escape_latex(parser.email) + r'}}'
        )
    if parser.phone:
        phone_href = re.sub(r'[\s\-\(\)\.]', '', parser.phone)
        contact_parts.append(
            r'\href{tel:' + phone_href + r'}{\underline{' + escape_latex(parser.phone) + r'}}'
        )
    return contact_parts


